package com.jack.learnDay6;

public class SimpleArray {
    public static void main(String[] args) {

        int[] bb = new int[5];
        bb[0] = 1;
        for (int i = 0; i < 5; i++) {
            System.out.print(bb[i]);
        }




        int[] cc = {1,2,3,4};
        for (int i = 0; i < 4; i++) {
            System.out.print(cc[i]);
        }


    }
}
